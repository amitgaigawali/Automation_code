class ArithmeticClass:
    def add(self, x, y):
        print('Adding two number {} & {} ' .format(x, y))
        res = x + y
        return res

    def mod(self, x, y):
        print('mod of two number {} & {}' .format(x, y))
        res = x % y
        return res

    def product(self, x, y):
        print('multiplication of two number  {} % {} ' .format(x, y))
        res = x * y
        return res
